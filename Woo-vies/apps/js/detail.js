function change_avengers() {
    var button = document.getElementById("add");
    if (button.value == "Add") {
        button.value = "Remove";
        button.innerHTML = "Remove from Watchlist";
        //add();

    }

    else {
        button.value = "Add";
        button.innerHTML = "Add to Watchlist";
        //remove();
    }

}

